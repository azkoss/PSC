package cn.wellcare.card.bo.card;

import java.io.Serializable;

public class CardInfo implements Serializable {

    private String MPI; //主索引编号
    private String cardRegCode; // 发卡登记编码
    private int sortNo; //卡序号
    private String accountCode; //账户编码

    public String getMPI() {
        return MPI;
    }

    public void setMPI(String MPI) {
        this.MPI = MPI;
    }

    public String getCardRegCode() {
        return cardRegCode;
    }

    public void setCardRegCode(String cardRegCode) {
        this.cardRegCode = cardRegCode;
    }

    public int getSortNo() {
        return sortNo;
    }

    public void setSortNo(int sortNo) {
        this.sortNo = sortNo;
    }

    public String getAccountCode() {
        return accountCode;
    }

    public void setAccountCode(String accountCode) {
        this.accountCode = accountCode;
    }
}
