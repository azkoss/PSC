package cn.wellcare.card.bo.card;

import java.io.Serializable;

public class CardOperateInfo implements Serializable {

    /**
     * 卡号
     */
    private String cardNo;

    /**
     * 账户密码
     */
    private String accountPassword;

    /**
     * 操作类型
     */
    private String operateType;

    /**
     * 操作人的
     */
    private String operateId;

    /**
     * 发卡登记编码
     */
    private String cardRegCode;

    /**
     *  补卡支付流水
     */
    private String paySerialId;

    private String newCardNo;

    private String innerNo;

    /**
     *  身份证号
     */
    private String credentialNo;

    public String getCardNo() {
        return cardNo;
    }

    public void setCardNo(String cardNo) {
        this.cardNo = cardNo;
    }

    public String getAccountPassword() {
        return accountPassword;
    }

    public void setAccountPassword(String accountPassword) {
        this.accountPassword = accountPassword;
    }

    public String getOperateType() {
        return operateType;
    }

    public void setOperateType(String operateType) {
        this.operateType = operateType;
    }

    public String getOperateId() {
        return operateId;
    }

    public void setOperateId(String operateId) {
        this.operateId = operateId;
    }

    public String getCardRegCode() {
        return cardRegCode;
    }

    public void setCardRegCode(String cardRegCode) {
        this.cardRegCode = cardRegCode;
    }

    public String getPaySerialId() {
        return paySerialId;
    }

    public void setPaySerialId(String paySerialId) {
        this.paySerialId = paySerialId;
    }

    public String getNewCardNo() {
        return newCardNo;
    }

    public void setNewCardNo(String newCardNo) {
        this.newCardNo = newCardNo;
    }

    public String getCredentialNo() {
        return credentialNo;
    }

    public void setCredentialNo(String credentialNo) {
        this.credentialNo = credentialNo;
    }

    public String getInnerNo() {
        return innerNo;
    }

    public void setInnerNo(String innerNo) {
        this.innerNo = innerNo;
    }
}
